#include "ft_printf.h"

void	ft_putchar(int c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		ft_putstr("(null)");
		return ;
	}
	while (s[i])
	{
		ft_putchar(s[i]);
		i++;
	}
}
